/// Generates the AppStringsKeys constants class
String generateAppStringsKeysClass(List<String> keys) {
  final buffer = StringBuffer();

  // File header
  buffer.writeln('// GENERATED CODE - DO NOT MODIFY BY HAND');
  buffer.writeln('// Generated by tools/generate_keys.dart');
  buffer.writeln('// Run: dart run tools/generate_keys.dart');
  buffer.writeln('');
  buffer.writeln('/// Auto-generated localization keys constants.');
  buffer.writeln('/// Provides type-safe access to localization keys.');
  buffer.writeln('class AppStringsKeys {');
  buffer.writeln('  AppStringsKeys._();');
  buffer.writeln('');

  // Generate constants — track used names to avoid duplicate_definition errors
  final usedNames = <String, int>{};
  for (final key in keys) {
    String constantName = _toCamelCase(key);
    if (usedNames.containsKey(constantName)) {
      final count = usedNames[constantName]! + 1;
      usedNames[constantName] = count;
      constantName = '$constantName$count';
    } else {
      usedNames[constantName] = 1;
    }
    buffer.writeln('  /// Key: "$key"');
    buffer.writeln('  static const String $constantName = "$key";');
    buffer.writeln('');
  }

  buffer.writeln('}');

  return buffer.toString();
}

/// Converts snake_case, kebab-case, or space-separated strings to camelCase
String _toCamelCase(String input) {
  // If input is already in valid camelCase format, return as-is (after reserved word check)
  if (_isValidCamelCase(input)) {
    // Check if it's a Dart reserved word and prefix if needed
    if (_isDartReservedWord(input)) {
      return 'key${input[0].toUpperCase()}${input.substring(1)}';
    }
    return input;
  }

  // Split by spaces, underscores, hyphens, forward slashes, and other special characters
  final parts = input.split(RegExp(r'[_\s\-\/\\\.,;:!@#$%^&*()+={}\[\]|<>?`~]+'));
  if (parts.isEmpty) return 'unknownKey';

  // Filter out empty parts
  final nonEmptyParts = parts.where((part) => part.isNotEmpty).toList();
  if (nonEmptyParts.isEmpty) return 'unknownKey';

  String result = nonEmptyParts.first.toLowerCase();

  for (int i = 1; i < nonEmptyParts.length; i++) {
    final part = nonEmptyParts[i];
    if (part.isNotEmpty) {
      result += part[0].toUpperCase() + part.substring(1).toLowerCase();
    }
  }

  // Check if result is a Dart reserved word and prefix if needed
  if (_isDartReservedWord(result)) {
    result = 'key${result[0].toUpperCase()}${result.substring(1)}';
  }

  // Ensure the result is a valid Dart identifier
  if (result.isEmpty || !RegExp(r'^[a-zA-Z_][a-zA-Z0-9_]*$').hasMatch(result)) {
    return 'key${result.hashCode.abs()}'; // Fallback to hash-based name
  }

  return result;
}

/// Checks if a string is already in valid camelCase format
bool _isValidCamelCase(String input) {
  // Must start with lowercase letter or underscore
  // Can contain letters, numbers, but no special characters or spaces
  // Should not contain underscores, spaces, or other separators (pure camelCase)
  return RegExp(r'^[a-z_][a-zA-Z0-9]*$').hasMatch(input) &&
         !input.contains('_') &&
         !input.contains(' ') &&
         !input.contains('-');
}

/// Checks if a string is a Dart reserved word
bool _isDartReservedWord(String word) {
  const dartReservedWords = {
    'abstract', 'as', 'assert', 'async', 'await', 'break', 'case', 'catch',
    'class', 'const', 'continue', 'covariant', 'default', 'deferred', 'do',
    'dynamic', 'else', 'enum', 'export', 'extends', 'extension', 'external',
    'factory', 'false', 'final', 'finally', 'for', 'function', 'get', 'hide',
    'if', 'implements', 'import', 'in', 'interface', 'is', 'late', 'library',
    'mixin', 'new', 'null', 'on', 'operator', 'part', 'required', 'rethrow',
    'return', 'set', 'show', 'static', 'super', 'switch', 'sync', 'this',
    'throw', 'true', 'try', 'typedef', 'var', 'void', 'while', 'with', 'yield',
    // Common types that might cause conflicts
    'String', 'int', 'double', 'bool', 'List', 'Map', 'Set', 'Object', 'Function'
  };

  return dartReservedWords.contains(word);
}