import 'dart:io';

/// Code generation script to extract localization keys from translationsMap
/// and generate a constants class for type-safe key access.
void main() async {
  print('🚀 Generating localization keys...');

  try {
    final appStringsTranslationFile = File('lib/localization/translations_map.dart');

    if (!await appStringsTranslationFile.exists()) {
      print('❌ AppStringsTranslation file not found at: ${appStringsTranslationFile.path}');
      exit(1);
    }

    final content = await appStringsTranslationFile.readAsString();
    final stringsTranslationKeys = await extractKeysFromStringsEntries(content);

    final allKeys = stringsTranslationKeys.toSet().toList()..sort();

    if (allKeys.isEmpty) {
      print('⚠️  No localization keys found in translations_map.dart');
      return;
    }

    print('📊 Total unique keys: ${allKeys.length}');

    final generatedCode = generateAppStringsKeysClass(allKeys);

    // Write to generated file
    final outputFile = File('lib/localization/generated/app_strings_keys.dart');
    await outputFile.parent.create(recursive: true);
    await outputFile.writeAsString(generatedCode);

    print('✅ Generated ${allKeys.length} keys in: ${outputFile.path}');
    print('📦 Keys generated: ${allKeys.join(', ')}');

  } catch (e) {
    print('❌ Error generating keys: $e');
    exit(1);
  }
}

/// Extracts localization keys from translationsMap in translations_map.dart
Future<List<String>> extractKeysFromStringsEntries(String content) async {
  final keys = <String>[];

  // Find the translationsMap Map content
  final translationsMapRegex = RegExp(
    r'final\s+Map<String,\s*TranslationModel>\s+translationsMap\s*=\s*\{(.*?)\};',
    multiLine: true,
    dotAll: true,
  );

  final match = translationsMapRegex.firstMatch(content);
  if (match == null) {
    print('❌ Could not find translationsMap');
    return keys;
  }

  final entriesContent = match.group(1)!;
  print('📋 Found translationsMap content');

  // Extract key-value pairs: "key": TranslationModel(...)
  final keyRegex = RegExp(
    r'"([^"]+)"\s*:\s*TranslationModel\(',
    multiLine: true,
  );

  for (final m in keyRegex.allMatches(entriesContent)) {
    final key = m.group(1)!;
    keys.add(key);
    print('🔑 Found key: $key');
  }

  return keys..sort();
}

